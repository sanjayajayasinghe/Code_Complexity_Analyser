package Models;

public class ScoreObject {

    private int CS = 0;
    private int CNC = 0;
    private int CI = 0 ;
    private int TW = 0;
    private int CPS = 0;
    private int CR = 0;
    int lineNo;

    public int getCS() {
        return CS;
    }

    public void setCS(int CS) {
        this.CS = CS;
    }

    public int getCNC() {
        return CNC;
    }

    public void setCNC(int CNC) {
        this.CNC = CNC;
    }

    public int getCI() {
        return CI;
    }

    public void setCI(int CI) {
        this.CI = CI;
    }

    public int getTW() {
        return TW;
    }

    public void setTW(int TW) {
        this.TW = TW;
    }

    public int getCPS() {
        return CPS;
    }

    public void setCPS(int CPS) {
        this.CPS = CPS;
    }

    public int getCR() {
        return CR;
    }

    public void setCR(int CR) {
        this.CR = CR;
    }

    public int getLineNo() {
        return lineNo;
    }

    public void setLineNo(int lineNo) {
        this.lineNo = lineNo;
    }
}
